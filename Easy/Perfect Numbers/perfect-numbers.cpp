    int isPerfectNumber(long long N) {
        // code here
        long long pns[] = {6l,28l,496l,8128l,33550336l,8589869056l,137438691328l};
        for (long long pn : pns) {
            if (pn == N) return 1;
        }
        return 0;
    }
